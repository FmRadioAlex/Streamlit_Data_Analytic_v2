import streamlit as st
import pandas as pd
import os
from datetime import datetime

DATA_FILE = "silver_data.csv"
LOG_FILE = "logs.csv"


if not os.path.exists(DATA_FILE):
    df = pd.DataFrame(columns=["Date", "Nick", "Silver", "Given"])
    df.to_csv(DATA_FILE, index=False)
else:
    df = pd.read_csv(DATA_FILE)

if not os.path.exists(LOG_FILE):
    logs_df = pd.DataFrame(columns=["Time", "User", "Action", "Nick", "Silver"])
    logs_df.to_csv(LOG_FILE, index=False)
else:
    logs_df = pd.read_csv(LOG_FILE)


st.set_page_config(page_title="Silver Manager", page_icon="üí∞", layout="centered")

if "authenticated" not in st.session_state:
    st.session_state.authenticated = False
    st.session_state.user = None

if not st.session_state.authenticated:
    nick = st.text_input("–ù—ñ–∫:")
    pwd = st.text_input("–ü–∞—Ä–æ–ª—å:", type="password")

    if st.button("–£–≤—ñ–π—Ç–∏"):
        users = st.secrets["database"]["users"]

        if nick in users and pwd == users[nick]:
            st.session_state.authenticated = True
            st.session_state.user = nick

            
            if os.path.exists(LOG_FILE):
                logs_df = pd.read_csv(LOG_FILE)
            else:
                logs_df = pd.DataFrame(columns=["Time", "User", "Action", "Nick", "Silver"])

            new_entry = {
                "Time": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                "User": nick,
                "Action": "–£—Å–ø—ñ—à–Ω–∏–π –≤—Ö—ñ–¥ —É —Å–∏—Å—Ç–µ–º—É",
                "Nick": "",
                "Silver": ""
            }
            logs_df = pd.concat([logs_df, pd.DataFrame([new_entry])], ignore_index=True)
            logs_df.to_csv(LOG_FILE, index=False)

            st.success(f"‚úÖ –í—Ö—ñ–¥ –≤–∏–∫–æ–Ω–∞–Ω–æ ({nick})")
            st.rerun()
        else:
            st.error("‚ùå –ù–µ–≤—ñ—Ä–Ω–∏–π –Ω—ñ–∫ –∞–±–æ –ø–∞—Ä–æ–ª—å.")
    st.stop()



def log_action(user, action, nick=None, silver=None):
    entry = {
        "Time": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "User": user,
        "Action": action,
        "Nick": nick if nick else "",
        "Silver": silver if silver else ""
    }

    logs_df = pd.read_csv(LOG_FILE)
    logs_df = pd.concat([logs_df, pd.DataFrame([entry])], ignore_index=True)
    logs_df.to_csv(LOG_FILE, index=False)

tabs = st.tabs(["üí∞ –ì–æ–ª–æ–≤–Ω–∞", "üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞", "üßæ –õ–æ–≥–∏"])

with tabs[0]:
    st.write("üîê –£–≤—ñ–π—à–æ–≤ —è–∫:", st.session_state.user)
    st.header("üí∞ Silver Manager")

    st.sidebar.subheader("üìÇ –ó–∞–≤–∞–Ω—Ç–∞–∂–∏—Ç–∏ CSV")
    uploaded_file = st.sidebar.file_uploader("–û–±–µ—Ä—ñ—Ç—å —Ñ–∞–π–ª CSV", type="csv")

    if uploaded_file:
        df = pd.read_csv(uploaded_file)
        df.to_csv(DATA_FILE, index=False)
        st.sidebar.success("‚úÖ –§–∞–π–ª –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–æ —Ç–∞ –∑–±–µ—Ä–µ–∂–µ–Ω–æ!")
        log_action(st.session_state.user, "–ó–∞–≤–∞–Ω—Ç–∞–∂–∏–≤ CSV —Ñ–∞–π–ª")

    with st.sidebar:
        st.subheader("‚ûï –î–æ–¥–∞—Ç–∏ –∫–æ–º–ø–µ–Ω—Å–∞—Ü—ñ—é")
        with st.form("add_form"):
            nick = st.text_input("–ù—ñ–∫")
            silver = st.number_input("–°—É–º–∞ Silver", min_value=0, step=10000)
            date_input = st.date_input("–î–∞—Ç–∞", datetime.now().date())
            submitted = st.form_submit_button("–î–æ–¥–∞—Ç–∏")

            if submitted:
                if nick and silver > 0:
                    new_row = {
                        "Date": date_input.strftime("%Y-%m-%d"),
                        "Nick": nick.strip(),
                        "Silver": silver,
                        "Given": False
                    }
                    df = pd.concat([df, pd.DataFrame([new_row])], ignore_index=True)
                    df.to_csv(DATA_FILE, index=False)
                    log_action(st.session_state.user, "–î–æ–¥–∞–≤ –∫–æ–º–ø–µ–Ω—Å–∞—Ü—ñ—é", nick, silver)
                    st.success(f"‚úÖ –î–æ–¥–∞–Ω–æ {nick} ({silver:,} silver)")
                else:
                    st.warning("‚ö†Ô∏è –í–≤–µ–¥–∏ –Ω—ñ–∫ —ñ —Å—É–º—É!")

        st.subheader("‚úÖ –í—ñ–¥–º—ñ—Ç–∏—Ç–∏ —è–∫ –≤–∏–¥–∞–Ω–æ–≥–æ")
        not_given = df[df["Given"] == False]

        if not not_given.empty:
            selected_nick = st.selectbox("–û–±–µ—Ä—ñ—Ç—å –Ω—ñ–∫", not_given["Nick"].unique())
            sum_silver = not_given[not_given["Nick"] == selected_nick]["Silver"].sum()
            st.write(f"–°—É–º–∞ –¥–æ –≤–∏–¥–∞—á—ñ: **{int(sum_silver):,} silver**")
            if st.button("–í–∏–¥–∞–Ω–æ ‚úÖ"):
                df.loc[df["Nick"] == selected_nick, "Given"] = True
                df.to_csv(DATA_FILE, index=False)
                log_action(st.session_state.user, "–í—ñ–¥–º—ñ—Ç–∏–≤ —è–∫ –≤–∏–¥–∞–Ω–æ–≥–æ", selected_nick, sum_silver)
                st.success(f"{selected_nick} –≤—ñ–¥–º—ñ—á–µ–Ω–æ —è–∫ –≤–∏–¥–∞–Ω–æ–≥–æ!")
                st.rerun()
        else:
            st.info("üéâ –£—Å—ñ –∫–æ–º–ø–µ–Ω—Å–∞—Ü—ñ—ó –≤–∏–¥–∞–Ω—ñ!")

    st.subheader("üìã –ü–æ—Ç–æ—á–Ω—ñ –∫–æ–º–ø–µ–Ω—Å–∞—Ü—ñ—ó")
    st.dataframe(df.style.format({"Silver": "{:,}"}), use_container_width=True)

    st.subheader("üóëÔ∏è –í–∏–¥–∞–ª–∏—Ç–∏ –∑–∞–ø–∏—Å")
    if not df.empty:
        df["Label"] = df.apply(lambda row: f"{row['Date']} | {row['Nick']} | {int(row['Silver']):,} silver", axis=1)
        row_to_delete = st.selectbox("–û–±–µ—Ä—ñ—Ç—å –∑–∞–ø–∏—Å –¥–ª—è –≤–∏–¥–∞–ª–µ–Ω–Ω—è:", df["Label"].tolist())

        if st.button("–í–∏–¥–∞–ª–∏—Ç–∏ –≤–∏–±—Ä–∞–Ω–∏–π –∑–∞–ø–∏—Å ‚ùå"):
            index_to_delete = df.index[df["Label"] == row_to_delete][0]
            deleted_row = df.loc[index_to_delete]
            df = df.drop(index_to_delete).drop(columns=["Label"])
            df.to_csv(DATA_FILE, index=False)
            log_action(st.session_state.user, "–í–∏–¥–∞–ª–∏–≤ –∑–∞–ø–∏—Å", deleted_row["Nick"], deleted_row["Silver"])
            st.success(f"üóëÔ∏è –í–∏–¥–∞–ª–µ–Ω–æ –∑–∞–ø–∏—Å: {deleted_row['Nick']} ({int(deleted_row['Silver']):,} silver)")
            st.rerun()
    else:
        st.info("–ù–µ–º–∞—î –¥–∞–Ω–∏—Ö –¥–ª—è –≤–∏–¥–∞–ª–µ–Ω–Ω—è.")



with tabs[1]:
    st.header("üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –∫–æ–º–ø–µ–Ω—Å–∞—Ü—ñ–π")
    if df.empty:
        st.info("–ù–µ–º–∞—î –¥–∞–Ω–∏—Ö –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è.")
    else:
        total_silver = df["Silver"].sum()
        given_count = df[df["Given"] == True].shape[0]
        not_given_count = df[df["Given"] == False].shape[0]

        col1, col2, col3 = st.columns(3)
        col1.metric("üíµ –í—Å—å–æ–≥–æ Silver", f"{int(total_silver):,}")
        col2.metric("‚úÖ –í–∏–¥–∞–Ω–æ", given_count)
        col3.metric("‚è≥ –ù–µ –≤–∏–¥–∞–Ω–æ", not_given_count)

        st.subheader("üèÜ –¢–æ–ø –æ—Ç—Ä–∏–º—É–≤–∞—á—ñ–≤ (–∑–∞ —Å—É–º–æ—é)")
        top_chart = df.groupby("Nick")["Silver"].sum().sort_values(ascending=False).head(10)
        st.bar_chart(top_chart)



with tabs[2]:
    st.header("üßæ –Ü—Å—Ç–æ—Ä—ñ—è –¥—ñ–π –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤")

